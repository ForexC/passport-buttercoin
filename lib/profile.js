/**
 * Parse profile.
 *
 * @param {Object|String} json
 * @return {Object}
 * @api private
 */
exports.parse = function(json) {
  if ('string' == typeof json) {
    json = JSON.parse(json);
  }

  var profile = {};
  profile.provider = 'buttercoin';
  profile.hashedAccountId = String(json.hashedAccountId);

  return profile;
};
